def translate_zh_cn(message: str) -> str | None:
    """Return a local Simplified Chinese translation for exact or patterned UI text."""
    if message in ZH_CN_FALLBACKS:
        return ZH_CN_FALLBACKS[message]

    stripped = message.strip()
    if stripped and stripped != message:
        translated = translate_zh_cn(stripped)
        if translated is not None:
            return message.replace(stripped, translated, 1)

    if re.fullmatch(r"[A-Z0-9]+ file input", message):
        return f"{message.removesuffix(' file input')} 文件输入"

    manual_input = re.fullmatch(r"convert ([A-Z0-9]+) file to Ink/Stitch manual-stitch paths", message)
    if manual_input:
        return f"将 {manual_input.group(1)} 文件转换为 Ink/Stitch 手动针迹路径"

    embroidery_format = re.fullmatch(r"Ink/Stitch: (.+) Embroidery Format \((\.[^)]+)\)", message)
    if embroidery_format:
        return f"Ink/Stitch: {embroidery_format.group(1)} 刺绣格式 ({embroidery_format.group(2)})"

    color_format = re.fullmatch(r"Ink/Stitch: (.+) Color Format \((\.[^)]+)\)", message)
    if color_format:
        return f"Ink/Stitch: {color_format.group(1)} 颜色格式 ({color_format.group(2)})"

    stitch_format = re.fullmatch(r"Ink/Stitch: (.+) Stitch Format \((\.[^)]+)\)", message)
    if stitch_format:
        return f"Ink/Stitch: {stitch_format.group(1)} 针迹格式 ({stitch_format.group(2)})"

    text_format = re.fullmatch(r"Ink/Stitch: (.+) Format, Text File \((\.[^)]+)\)", message)
    if text_format:
        return f"Ink/Stitch: {text_format.group(1)} 格式，文本文件 ({text_format.group(2)})"

    generic_format = re.fullmatch(r"Ink/Stitch: (.+) Format \((\.[^)]+)\)", message)
    if generic_format:
        return f"Ink/Stitch: {generic_format.group(1)} 格式 ({generic_format.group(2)})"

    tagged_format = re.fullmatch(r"Ink/Stitch: (.+) \[([A-Z]+)\] \((\.[^)]+)\)", message)
    if tagged_format:
        name = tagged_format.group(1)
        name = name.replace("Embroidery Format", "刺绣格式")
        name = name.replace("Color Format", "颜色格式")
        name = name.replace("Stitch Format", "针迹格式")
        name = name.replace("Format, Text File", "格式，文本文件")
        tags = {
            "COLOR": "颜色",
            "DEBUG": "调试",
            "IMAGE": "图像",
            "STITCH": "针迹",
            "QUILTING": "绗缝",
        }
        tag = tags.get(tagged_format.group(2), tagged_format.group(2))
        return f"Ink/Stitch: {name} [{tag}] ({tagged_format.group(3)})"

    save_format = re.fullmatch(r"Save design in ([A-Z0-9]+) format using Ink/Stitch", message)
    if save_format:
        return f"使用 Ink/Stitch 以 {save_format.group(1)} 格式保存设计"

    return None
